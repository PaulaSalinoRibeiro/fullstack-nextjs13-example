'use server';

import { z } from 'zod';
import { sql } from '@vercel/postgres';
import { revalidatePath } from 'next/cache';
import { redirect } from 'next/navigation';

// React Server Actions allow you to run asynchronous code directly on the server. 
// They eliminate the need to create API endpoints to mutate your data. 
// Instead, you write asynchronous functions that execute on the server and can be 
// invoked from your Client or Server Components.
// Security is a top priority for web applications, as they can be vulnerable to various threats.
// This is where Server Actions come in. They offer an effective security solution, protecting 
// against different types of attacks, securing your data, and ensuring authorized access. 
